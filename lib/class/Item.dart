class Item {
  String idItem;
  String nameItem;
  String picturePath;
  String idShop;
  String priceItem;

  Item(String idItem, String nameItem, String picturePath, String idShop, String priceItem) {
    this.idItem = idItem;
    this.nameItem = nameItem;
    this.picturePath = picturePath;
    this.idShop = idShop;
    this.priceItem = priceItem;
  }
}